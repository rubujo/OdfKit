namespace OdfKit.DOM;

/// <summary>
/// 表示 ODF schema 中名為 <c>lineType</c> 的線條類型 token。
/// </summary>
public enum OdfLineType
{
    /// <summary>
    /// 無線條。
    /// </summary>
    None,

    /// <summary>
    /// 單線。
    /// </summary>
    Single,

    /// <summary>
    /// 雙線。
    /// </summary>
    Double
}
